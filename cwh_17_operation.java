public class cwh_17_operation {
    public static void main(String[] args) {
        System.out.println("AND Operation :");
        boolean a = true;
        boolean b = false;
        System.out.println(" Checking AND condition");
        if(a && b){
            System.out.println("Yes!");
        }
        else {
            System.out.println("No!");
        }
        System.out.println("-----------");
        System.out.println("For OR condition:");
        if (a || b ){
            System.out.println(" Yes!!");
        }
        else {
            System.out.println("No!!");
        }
        System.out.println("-----------");
        System.out.println(" For NOT(!) Condition :");
        System.out.print( "Not(a) is:");
        System.out.println(!a);
        System.out.print( "Not(b) is:");
        System.out.println(!b);
        System.out.println("-----------");


    }
}
